//onEachFeature is a function that takes feature and makes a layer and does something with that layer
//this one binds a popup on each feature if the feature has properties and one of those properties is "popupContent"
function oneach(feature, layer) {
    // does this feature have a property named popupContent?
    if (feature.properties && feature.properties.popupContent) {
        layer.bindPopup(feature.properties.popupContent);
    }
}

